#사칙연산
#사칙연산 +(더하기), -(빼기), *(곱하기), /(나누기)
#기호 앞과 뒤의 띄어쓰기는 신경 쓰지 않는다.
a=3
b=4
a+b
7
a*b
12
a/b
0.75

#제곱
#같은 수를 그 수만큼 곱하는 것을 말한다.
