81

#나눗셈 후 나머지 반환
#나눗셈의 나머지 값을 돌려주는 것을 말한다. 7을 3으로 나누면 몫은 2가 나오고 나머지 1이 비어있게 된다. 그때 그 나머지를 출력하는 것이 나머지 반환이다.
7%3
1
3%7
3

#나눗셈 후 몫 반환
#나눗셈의 몫을 반환한다. 위와 다르게 정수값만 돌려준다.
7/4
1.75
7//4
1